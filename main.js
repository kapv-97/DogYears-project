// My age is 24
const myAge = 24;

// this represents the first 2 years of a dog's life
var earlyYears = 2;

earlyYears = 2 * 10.5;

// this is the myAge minus 2
var laterYears = myAge - 2;

// this will equal the number in dog years accounted for by my later years
laterYears *= 4;

// equation to calculate my age in dog years    
myAgeInDogYears = earlyYears + laterYears;

// with this I turn my name to all lower case letters
const myName = 'Kimberly'.toLowerCase();

// result of the calculations made to estimate what my dog years would be 
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);
